package ru.job4j.inheritance;

public class Grade {
    private int grade;
    private String science;

    public void setGrade(int grade) {
        this.grade = grade;
    }

    public int getGrade() {
        return grade;
    }

    public void setScience(String science) {
        this.science = science;
    }

    public String getScience() {
        return science;
    }
}
